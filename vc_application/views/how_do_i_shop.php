<b>Shopping from us is easy!</b>

<h4>Select the product you’d like to buy. Create a login & proceed </h4>
<p>Browse or search for the items you would like to order. Keyword search box "I'm looking for" is located at the top of the site. You will also find a list of categories under the navigation bar.</p>

<h4>Add products to your cart or just hit Buy Now or Pay by Bliss Cash</h4>

<p>While on the product details page, select the specification (if applicable) in the dropdown box, and click on "Add to Bag" button or just hit ‘Buy Now’ or Pay by Bliss Cash or Pre Order (If applicable). Once you've added an item to your Shopping Bag, you can continue to search or browse the site until your bag contains all of the items you want to order.<p>

<h4>Proceed to checkout</h4>

<p>Click on "My shopping bag" icon placed on the top right hand corner of the page. Take a moment to review all of the items you've placed in your Shopping Bag & Click on "Proceed to checkout" button.<p>

<h4>Choose or Add delivery address</h4>

<p>Tell us where you would like us to ship your order. Click on "Continue" button to proceed.
Add a working phone number and email address.<p>

<h4>Check order details</h4>

<p>Verify the item selected, the shipping address provided and the amount payable. Click on "Continue" button to proceed.


<p>Use a preferred payment mode and confirm the order
Select from the available payment options and click on "Place Order" button to complete your order.

Happy  Shopping.

